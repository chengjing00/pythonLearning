# int type
num1 = 1
# float
num2 = 1.1
print(type(num1))
print(type(num2))

# bool
b = True
print(type(b))

# str
str = "hello world"
print(type(str))

# list -- 列表
c = [10, 20, 30]
print(type(c))

# tuple -- 元组
d = (10, 20, 30)
print(type(d))

# set -- 集合
s = {10, 20, 30}
print(type(s))

# dict -- 字典 -- 键值对
f = {"name": "tom", "age": 10}
print(type(f))



